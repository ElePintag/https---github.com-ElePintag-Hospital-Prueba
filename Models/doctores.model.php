<?php
require_once('cls_conexion.model.php');
class Clase_Doctores
{
    public function todos()
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT doctores.ID_doctor, hospitales.Nombre as hospital, doctores.Nombre, doctores.Especialidad, doctores.Salario FROM `doctores` INNER JOIN hospitales ON hospitales.ID_hospital = doctores.ID_hospital";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function uno($ID_doctor)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `doctores` WHERE ID_doctor=$ID_doctor";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function insertar($ID_hospital, $Nombre, $Especialidad, $Salario)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "INSERT INTO `doctores`(`ID_hospital`, `Nombre`, `Especialidad`, `Salario`) VALUES ('$ID_hospital', '$Nombre', '$Especialidad', '$Salario')";
            $result = mysqli_query($con, $cadena);
            return 'ok';
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function actualizar($ID_doctor, $ID_hospital, $Nombre, $Especialidad, $Salario)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "UPDATE `doctores` SET `ID_hospital`='$ID_hospital', `Nombre`='$Nombre', `Especialidad`='$Especialidad', `Salario`='$Salario' WHERE `ID_doctor`='$ID_doctor'";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function eliminar($ID_doctor)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "DELETE FROM doctores WHERE ID_doctor=$ID_doctor";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
}
